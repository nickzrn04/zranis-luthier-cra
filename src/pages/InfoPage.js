export default function InfoPage(props) {
  const [image,setImage] = React.useState(0);
  const pictures = ["../icons/μαγαζι-μεσα.jpg","../icons/guitar.jpg","../icons/φωνη.jpeg"]
  function next() {
    if(image+1===pictures.length) {
      setImage(0);
    } else setImage(image+1)
  }
  function prev() {
    if(image===0) {
      setImage(pictures.length-1);
    } else setImage(image-1)
  }

  return (
    <div>
      <main className="hero" style={{display: "flex", flexDirection: "row", alignItems: "center", justifyContent: "center"}}>
        <div className="someInfo">
          {props.language=='gr'?grTxt():enTxt()}
        </div>
        <div className="text--images">
          <img src="./icons/προφιλ.jpg"></img>
          <img src="./icons/ουτι.jpg"></img>
        </div>
      </main>
      <div className="infoImg">
        <img id="infoPictures" src={`${pictures[image]}`}/>
        <button onClick={next} className="nextR">&#x3e;</button>
        <button onClick={prev} className="nextL">&#x3c;</button>
      </div>

    </div>
  )
}